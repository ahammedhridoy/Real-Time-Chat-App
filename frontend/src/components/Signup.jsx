import React, { useState } from "react";
import { Link, useNavigate } from "react-router-dom";
import axios from "axios";
import toast from "react-hot-toast";
import { BASE_URL } from "..";

const Signup = () => {
  const [user, setUser] = useState({
    fullName: "",
    username: "",
    password: "",
    confirmPassword: "",
    gender: "",
  });
  const navigate = useNavigate();
  const handleCheckbox = (gender) => {
    setUser({ ...user, gender });
  };
  const onSubmitHandler = async (e) => {
    e.preventDefault();
    try {
      const res = await axios.post(`${BASE_URL}/api/v1/user/register`, user, {
        headers: {
          "Content-Type": "application/json",
        },
        withCredentials: true,
      });
      if (res.data.success) {
        navigate("/login");
        toast.success(res.data.message);
      }
    } catch (error) {
      toast.error(error.response.data.message);
      console.log(error);
    }
    setUser({
      fullName: "",
      username: "",
      password: "",
      confirmPassword: "",
      gender: "",
    });
  };
  return (
    <div className="mx-auto min-w-96">
      <div className="w-full p-6 bg-gray-400 border border-gray-100 rounded-lg shadow-md bg-clip-padding backdrop-filter backdrop-blur-md bg-opacity-10">
        <h1 className="text-3xl font-bold text-center text-white">Signup</h1>
        <form onSubmit={onSubmitHandler} action="">
          <div>
            <label className="p-2 label">
              <span className="text-base text-white label-text">Full Name</span>
            </label>
            <input
              value={user.fullName}
              onChange={(e) => setUser({ ...user, fullName: e.target.value })}
              className="w-full h-10 input input-bordered"
              type="text"
              placeholder="Full Name"
            />
          </div>
          <div>
            <label className="p-2 label">
              <span className="text-base text-white label-text">Username</span>
            </label>
            <input
              value={user.username}
              onChange={(e) => setUser({ ...user, username: e.target.value })}
              className="w-full h-10 input input-bordered"
              type="text"
              placeholder="Username"
            />
          </div>
          <div>
            <label className="p-2 label">
              <span className="text-base text-white label-text">Password</span>
            </label>
            <input
              value={user.password}
              onChange={(e) => setUser({ ...user, password: e.target.value })}
              className="w-full h-10 input input-bordered"
              type="password"
              placeholder="Password"
            />
          </div>
          <div>
            <label className="p-2 label">
              <span className="text-base text-white label-text">
                Confirm Password
              </span>
            </label>
            <input
              value={user.confirmPassword}
              onChange={(e) =>
                setUser({ ...user, confirmPassword: e.target.value })
              }
              className="w-full h-10 input input-bordered"
              type="password"
              placeholder="Confirm Password"
            />
          </div>
          <div className="flex items-center my-4">
            <div className="flex items-center">
              <p className="text-white">Male</p>
              <input
                type="checkbox"
                checked={user.gender === "male"}
                onChange={() => handleCheckbox("male")}
                defaultChecked
                className="mx-2 border-[#4E6688] checkbox border-1"
              />
            </div>
            <div className="flex items-center">
              <p className="text-white">Female</p>
              <input
                type="checkbox"
                checked={user.gender === "female"}
                onChange={() => handleCheckbox("female")}
                defaultChecked
                className="mx-2 border-[#4E6688] checkbox border-1"
              />
            </div>
          </div>
          <p className="my-2 text-center text-white">
            Already have an account?{" "}
            <Link to="/login" className="text-blue-500">
              {" "}
              login{" "}
            </Link>
          </p>
          <div>
            <button
              type="submit"
              className="mt-2 border btn btn-block btn-sm border-slate-700 bg-[#71C0BB] text-white"
            >
              Singup
            </button>
          </div>
        </form>
      </div>
    </div>
  );
};

export default Signup;
